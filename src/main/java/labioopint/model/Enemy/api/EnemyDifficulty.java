package labioopint.model.Enemy.api;

/**
 * The {@code EnemyDifficulty} enum represents the difficulty levels for enemies
 * in the game.
 * Each difficulty level determines the behavior and complexity of the enemy's
 * actions.
 */
public enum EnemyDifficulty {
    /**
     * Easy difficulty level for enemies.
     */
    EASY,

    /**
     * Medium difficulty level for enemies.
     */
    MEDIUM,

    /**
     * Hard difficulty level for enemies.
     */
    HARD
}
